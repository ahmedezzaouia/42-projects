int	ft_isalnum(int c)
{
	unsigned char	k;

	k = c;
	if (!(k >= '0' && k <= '9'))
		if (!(k >= 65 && k <= 90))
			if (!(k >= 97 && k <= 122))
				return (0);
	return (1);
}
